int	check_env_sep(char c)
{
	if (c == ' ' || c == '*' || c == '$')
		return (1);
	return (0);
}

int check_str_wildcard(char *s)
{
	while (*s)
	{
		if (*s == '*')
			return (1);
		s++;
	}
	return (0);
}
